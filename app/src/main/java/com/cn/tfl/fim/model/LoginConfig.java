package com.cn.tfl.fim.model;

/**
 * 登录配置信息
 */
public class LoginConfig {

    public String xmppHost;//地址
    public Integer xmppPort;//端口
    public String xmppServiceName;//服务器名称
    public String username;//用户名
    public String password;//密码
    public boolean isRemember;//是否记住密码
    public boolean isAutoLogin;//是否自动登录
    public boolean isNovisible;//是否隐身登录
    public boolean isOnline;//是否在线
    public boolean isFirstStart;//是否第一次启动
}
